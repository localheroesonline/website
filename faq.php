<h3>Voorbeeld 2:</h3>

<p>Je plaatst een bestelling ter waarde van €10,-. Stel dat jouw Local Heroes puntensaldo op het moment van de eerstvolgende bestelling 50 is (een positief saldo), dan wordt er €0,50 afgetrokken van jouw totaalbedrag. Je rekent nu €9,50 af in plaats van €10,- omdat je nog een positieve puntensaldo had.</p>

<h3>Voorbeeld 3:</h3>

<p>Je plaatst een bestelling ter waarde van €10,-. Stel dat jouw Local Heroes puntensaldo op het moment van de eerstvolgende bestelling -50 is (een negatief saldo), dan wordt er €0,50 opgeteld bij het totaalbedrag van jouw bestelling. Je rekent nu €10,50 af in plaats van €10,- omdat je nog een negatief puntensaldo had.
